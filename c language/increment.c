#include<stdio.h>

int main1()
{int num = 3;
    printf("using unary prefix increament operator\n\n");
printf("The given value is : %d\n\n", num);
printf("The value of ++num is %d\n\n", ++num);
printf("The new value of num is: %d\n\n", num);
printf("Using unary prefix decreament operator\n\n");
printf("The given value is : %d\n\n", num); 
printf("The value of --num is : %d\n\n", --num); 
printf("The new value of num is : %d\n\n", num); 



    return 0;
}
